#define SIGNATURE_VERIFY_SIZE (uint32)512

uint8 OutBuffer[16] = {0};
uint32 SheHashAddr = 0x7010F000;
uint32 SheHashSize = 0x00000800;
static uint8 Routine_DD02_NRC78_Flag = 0;
static uint8 Routine_DD02_Sign_Verify_Step = 0;
static uint8 TestRSA_VerifyResult = 0;
static uint32 rc_size_to_be_Verify = 0;
static uint32 rc_addr_to_be_Verify = 0;
static uint32 HsmCurrentSt;
void VerifySignature(void)
{
	Csm_ReturnType test_RetVal=CSM_E_NOT_OK;
	uint8 Head[8] = {0};

	*OutBuffer = 0x0;

	if(Routine_DD02_NRC78_Flag == 0)
    {
        Routine_DD02_NRC78_Flag = 1u;
        Routine_DD02_Sign_Verify_Step = 0u; 
        print_f("\n please call again                \n");
    }
    else
    {
            switch (Routine_DD02_Sign_Verify_Step)
            {
                case 0:
                    /*load key*/
                    test_RetVal = SampleCode_LoadPlainKey(PLAIN_KEY_13,RSA3072_publicExponent_1,4); /*rsa3072 public key*/
                    test_RetVal = SampleCode_LoadPlainKey(PLAIN_KEY_14,RSA3072_modulusExponent_1,384); /*rsa3072 modulus key*/

                    Routine_DD02_Sign_Verify_Step = 1;
                    print_f("\n load key,please call again                \n");
                break;
                case 1:
                    /*add Head*/
                    Head[0] = SheHashAddr >> 24;
                    Head[1] = (SheHashAddr >> 16) & 0xFF;
                    Head[2] = (SheHashAddr >> 8) & 0xFF;
                    Head[3] = SheHashAddr & 0xFF;
                    Head[4] = SheHashSize >> 24;
                    Head[5] = (SheHashSize >> 16) & 0xFF;
                    Head[6] = (SheHashSize >> 8) & 0xFF;
                    Head[7] = SheHashSize & 0xFF;

                    /*RSA update Head*/
                    test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,NULL_PTR, 0,
                    													 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_START);
                    test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,Head, 8,
                    													 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_UPDATE);
                    Routine_DD02_Sign_Verify_Step = 2;
                    print_f("\n add head,please call again                \n");
                break;
                case 2:
                    /*Verify First Block*/
                    if(SheHashSize >= SIGNATURE_VERIFY_SIZE)
                    {
                        rc_size_to_be_Verify = SheHashSize;
                        rc_addr_to_be_Verify = (uint32)(FlashBin + 8);

						print_f("\n rc size to be verify:%x \n",rc_size_to_be_Verify);
						print_f("\n rc addr to be verify:%x \n",rc_addr_to_be_Verify);
			
                        test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,(uint8 *)rc_addr_to_be_Verify, SIGNATURE_VERIFY_SIZE,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_UPDATE);
						print_f("\n add 512 byte to verify\n");
                        rc_size_to_be_Verify -= SIGNATURE_VERIFY_SIZE;
                        rc_addr_to_be_Verify += SIGNATURE_VERIFY_SIZE;
                        Routine_DD02_Sign_Verify_Step = 3;
                        print_f("\n size > 512 ,please call again                \n");        
                    }
                    else
                    {
                        test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,(uint8 *)SheHashAddr, SheHashSize,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_UPDATE);

                        test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,NULL_PTR, 0,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_FINISH);
   
                        if(test_RetVal == CSM_E_OK&&TestRSA_VerifyResult==0)
						{
                            Routine_DD02_NRC78_Flag = 0;
                            *OutBuffer = 0;
                            print_f("\n size < 512,verify success \n");
                        }
                        else
                        {
                            Routine_DD02_NRC78_Flag = 0;
                            *OutBuffer = 1;
                            print_f("\n size < 512,verify faild \n");
                        }
                    }

                break;
                case 3:
                    if(SIGNATURE_VERIFY_SIZE >=rc_size_to_be_Verify)
                    {
                        if(0 != rc_size_to_be_Verify)
                        {
                            test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,(uint8 *)rc_addr_to_be_Verify, rc_size_to_be_Verify,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_UPDATE);
                            Routine_DD02_Sign_Verify_Step = 3;
                            rc_size_to_be_Verify = 0;
                            print_f("\n left size < 512,please call again \n");                           
                        }
                        else
                        {
                            /*the last one Block*/
                            test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,NULL_PTR, 0,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_FINISH);

                            if(test_RetVal == CSM_E_OK&&TestRSA_VerifyResult==0)
                            {
                                Routine_DD02_NRC78_Flag = 0;
                                *OutBuffer = 0;
                                print_f("\n left size = 0,verify success \n");
                            }
                            else
                            {
                                Routine_DD02_NRC78_Flag = 0;
                                *OutBuffer = 1;
                                print_f("\n left size = 0,verify faild \n");
                            }

                        }
                    }
                    else
                    {
						print_f("\n rc size to be verify:%x \n",rc_size_to_be_Verify);
						print_f("\n rc addr to be verify:%x \n",rc_addr_to_be_Verify);

                        test_RetVal = SampleCode_SignatureVerify(&PlainKey[12],HSM_SIGNATURE_ALGORITHM_RSA_v21,NULL_PTR,0,(uint8 *)rc_addr_to_be_Verify, SIGNATURE_VERIFY_SIZE,
															 RSA_Signature_1,384,&TestRSA_VerifyResult,CRYPTO_OPERATIONMODE_UPDATE);

						print_f("\n add 512 byte to verify\n");
                        rc_size_to_be_Verify -= SIGNATURE_VERIFY_SIZE;
                        rc_addr_to_be_Verify +=SIGNATURE_VERIFY_SIZE;
                        Routine_DD02_Sign_Verify_Step = 3;
                        print_f("\n size > 512 ,please call again                \n");
                    }
                break;    
                default:
                break;
            }
    }
}


